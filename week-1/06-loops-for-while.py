# Using a for loop to iterate over a tuple
fruits = ("apple", "banana", "cherry")
for fruit in fruits:
    print(fruit)

# Print each letter in a string
name = "Roni"
for letter in name:
    print(letter)

# Print numbers using range
for i in range(5):
    print(i)

# Colors example
colors = ("red", "green", "blue")
for color in colors:
    print(color)

# Using a while loop
count = 1
while count <= 5:
    print(count)
    count += 1
