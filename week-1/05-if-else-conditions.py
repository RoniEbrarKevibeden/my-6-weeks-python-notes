# Grade evaluation
grade = 85

if grade >= 90:
    print("Grade: A")
elif grade >= 80:
    print("Grade: B")
elif grade >= 70:
    print("Grade: C")
else:
    print("Grade: F")


# Age check
age = 18

if age >= 21:
    print("You can drink alcohol in the USA")
elif age >= 18:
    print("You can vote")
else:
    print("You are a minor")


# Number sign check
num = int(input("Enter a number: "))

if num < 0:
    print("Negative number")
elif num == 0:
    print("Zero")
else:
    print("Positive number")
